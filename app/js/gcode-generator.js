/**
 * G-code Generator for cycleCAD
 * Converts CAM toolpaths to G-code for CNC, FDM, and laser cutting.
 * IIFE pattern, registers on window.cycleCAD.gcode
 *
 * @author Sachin Kumar
 * @version 1.0
 */

(function() {
  'use strict';

  // ===== G-Code Presets & Macros =====
  const PRESETS = {
    'linuxcnc': {
      name: 'LinuxCNC',
      extension: '.ngc',
      header: ['G17 G90 G80', 'G0 Z0.5'],
      footer: ['M5', 'G0 Z0.5', 'M30'],
      coolant: 'M8'
    },
    'fanuc': {
      name: 'FANUC',
      extension: '.gcode',
      header: ['G92 X0 Y0 Z0', 'G17 G90 G80', 'G0 G54 Z0.5'],
      footer: ['M5', 'G0 Z0.5', 'M30'],
      coolant: 'M7'
    },
    'haas': {
      name: 'HAAS',
      extension: '.gcode',
      header: ['G92 X0 Y0 Z0', 'G17 G90 G80'],
      footer: ['M5', 'G0 Z0.5', 'M30'],
      coolant: 'M8'
    },
    'marlin': {
      name: 'Marlin (FDM)',
      extension: '.gcode',
      header: [
        'G28 ; home all axes',
        'G29 ; mesh bed leveling',
        'G92 E0 ; reset extrusion',
        'G0 Z0.2 F3000 ; nozzle height'
      ],
      footer: ['M104 S0 ; turn off hotend', 'M140 S0 ; turn off bed', 'M84 ; disable motors'],
      coolant: null
    },
    'laser': {
      name: 'Laser Cutter',
      extension: '.gcode',
      header: ['G17 G90 G80', 'M104 S0 ; laser off'],
      footer: ['M5', 'M30'],
      coolant: null
    }
  };

  // ===== Header & Footer Macro Templates =====
  const MACRO_TEMPLATES = {
    cnc_header: (rpm, tool) => [
      `; CNC Program Generated by cycleCAD`,
      `; Tool: ${tool || 'T1'}, Speed: ${rpm} RPM`,
      `; ${new Date().toISOString().slice(0, 10)}`,
      `G17 G90 G80`,
      `G0 Z0.5 F500`
    ],
    fdm_header: (nozzleTemp, bedTemp) => [
      `; FDM Program Generated by cycleCAD`,
      `; Nozzle: ${nozzleTemp}°C, Bed: ${bedTemp}°C`,
      `G28 ; home all axes`,
      `G29 ; mesh bed leveling`,
      `M109 S${nozzleTemp} ; wait for nozzle`,
      `M190 S${bedTemp} ; wait for bed`,
      `G92 E0 ; reset extrusion`
    ],
    laser_header: (power) => [
      `; Laser Program Generated by cycleCAD`,
      `; Max Power: ${power}%`,
      `G17 G90 G80`,
      `M106 S0 ; laser off`,
      `G0 Z0 F3000`
    ]
  };

  // ===== Convert Toolpath to G-code =====
  function toolpathToGCode(toolpath, params = {}) {
    const {
      mode = 'cnc', // 'cnc', 'fdm', 'laser'
      preset = 'linuxcnc',
      rpm = 8000,
      tool = 'T1',
      lineNumbers = true,
      addComments = true,
      nozzleTemp = 210,
      bedTemp = 60,
      laserPower = 100,
      materialFeedback = null
    } = params;

    if (!toolpath || toolpath.length === 0) {
      return { success: false, error: 'Empty toolpath', gcode: '' };
    }

    const presetData = PRESETS[preset] || PRESETS.linuxcnc;
    const lines = [];
    let lineNum = 10;

    // ===== Header =====
    if (mode === 'cnc') {
      const header = MACRO_TEMPLATES.cnc_header(rpm, tool);
      lines.push(...header);
    } else if (mode === 'fdm') {
      const header = MACRO_TEMPLATES.fdm_header(nozzleTemp, bedTemp);
      lines.push(...header);
    } else if (mode === 'laser') {
      const header = MACRO_TEMPLATES.laser_header(laserPower);
      lines.push(...header);
    }

    // ===== Body =====
    let lastPoint = null;
    let currentSpindle = null;
    let currentTool = null;
    let extrusionDistance = 0;

    for (let i = 0; i < toolpath.length; i++) {
      const point = toolpath[i];
      const nextPoint = toolpath[i + 1];

      let gcode = '';
      const comment = addComments ? ` ; Point ${i}` : '';

      // Tool change
      if (mode === 'cnc' && tool !== currentTool) {
        gcode = `M6 T${tool}`;
        lines.push(formatLine(gcode, lineNum, lineNumbers));
        lineNum += 10;
        currentTool = tool;
      }

      // Spindle control (CNC mode)
      if (mode === 'cnc' && !currentSpindle) {
        gcode = `M3 S${rpm}`;
        lines.push(formatLine(gcode, lineNum, lineNumbers));
        lineNum += 10;
        currentSpindle = true;
      }

      // Movement commands
      const { x, y, z, f, type } = point;

      if (type === 'G0') {
        // Rapid positioning
        gcode = `G0 X${x.toFixed(3)} Y${y.toFixed(3)} Z${z.toFixed(3)}`;
        lines.push(formatLine(gcode, lineNum, lineNumbers, comment));
      } else if (type === 'G1') {
        // Linear interpolation
        let feedCmd = `F${f.toFixed(0)}`;

        if (mode === 'fdm') {
          // FDM: calculate extrusion
          if (lastPoint) {
            const distance = Math.sqrt(
              Math.pow(x - lastPoint.x, 2) +
              Math.pow(y - lastPoint.y, 2) +
              Math.pow(z - lastPoint.z, 2)
            );
            extrusionDistance += distance * 0.5; // rough extrusion factor
            gcode = `G1 X${x.toFixed(3)} Y${y.toFixed(3)} Z${z.toFixed(3)} E${extrusionDistance.toFixed(3)} ${feedCmd}`;
          } else {
            gcode = `G1 X${x.toFixed(3)} Y${y.toFixed(3)} Z${z.toFixed(3)} ${feedCmd}`;
          }
        } else if (mode === 'laser') {
          // Laser: power control
          gcode = `G1 X${x.toFixed(3)} Y${y.toFixed(3)} Z${z.toFixed(3)} ${feedCmd} S${laserPower}`;
        } else {
          // CNC: standard
          gcode = `G1 X${x.toFixed(3)} Y${y.toFixed(3)} Z${z.toFixed(3)} ${feedCmd}`;
        }

        lines.push(formatLine(gcode, lineNum, lineNumbers, comment));
      } else if (type === 'G2' || type === 'G3') {
        // Arc interpolation (simplified for now)
        const { centerX = 0, centerY = 0, direction = 'cw' } = point;
        const arcType = direction === 'cw' ? 'G2' : 'G3';

        gcode = `${arcType} X${x.toFixed(3)} Y${y.toFixed(3)} I${centerX.toFixed(3)} J${centerY.toFixed(3)} F${f.toFixed(0)}`;
        lines.push(formatLine(gcode, lineNum, lineNumbers, comment));
      }

      lineNum += 10;
      lastPoint = { x, y, z };
    }

    // ===== Coolant Control =====
    if (presetData.coolant && mode === 'cnc') {
      lines.push(formatLine(presetData.coolant, lineNum, lineNumbers));
      lineNum += 10;
    }

    // ===== Spindle Off (CNC) =====
    if (mode === 'cnc') {
      lines.push(formatLine('M5', lineNum, lineNumbers));
      lineNum += 10;
    }

    // ===== Footer =====
    for (let footerLine of presetData.footer) {
      lines.push(formatLine(footerLine, lineNum, lineNumbers));
      lineNum += 10;
    }

    const gcode = lines.join('\n');
    const stats = calculateStats(toolpath);

    return {
      success: true,
      gcode: gcode,
      extension: presetData.extension,
      stats: stats
    };
  }

  // ===== Format Single G-code Line =====
  function formatLine(command, lineNum, addLineNum = true, comment = '') {
    let line = command;
    if (addLineNum) {
      line = `N${lineNum} ${line}`;
    }
    if (comment) {
      line += comment;
    }
    return line;
  }

  // ===== FDM-Specific Features =====
  function addFDMFeatures(gcode, params = {}) {
    const {
      retraction = true,
      retractionDistance = 5,
      retractionSpeed = 40,
      fanControl = true,
      fanSpeed = 255
    } = params;

    let enhanced = gcode;

    // Insert retraction commands at specified points (simplified)
    if (retraction) {
      enhanced = enhanced.replace(
        /G0 Z/g,
        `G10 ; retract\nG0 Z`
      );
      enhanced = enhanced.replace(
        /G1 Z/g,
        `G11 ; unretract\nG1 Z`
      );
    }

    // Add fan control
    if (fanControl) {
      const fanCmd = `M106 S${fanSpeed} ; fan on`;
      enhanced = enhanced.split('\n')[0] + '\n' + fanCmd + '\n' + enhanced.substring(enhanced.indexOf('\n') + 1);
    }

    return enhanced;
  }

  // ===== CNC-Specific Features =====
  function addCNCFeatures(gcode, params = {}) {
    const {
      coolant = true,
      coolantType = 'flood', // 'flood', 'mist', 'through'
      tooloffset = true,
      workOffset = 'G54'
    } = params;

    let enhanced = gcode;

    // Add work offset
    if (tooloffset) {
      enhanced = enhanced.replace(
        /G17 G90/,
        `${workOffset}\nG17 G90`
      );
    }

    // Add coolant type
    if (coolant) {
      const coolantCode = coolantType === 'mist' ? 'M7' : 'M8';
      enhanced = enhanced.replace(
        /M3 S\d+/,
        `M3 S$&\n${coolantCode} ; coolant on`
      );
    }

    return enhanced;
  }

  // ===== Laser-Specific Features =====
  function addLaserFeatures(gcode, params = {}) {
    const {
      powerScale = 1.0,
      focusHeight = 0,
      assistGas = true
    } = params;

    let enhanced = gcode;

    // Scale all power values
    if (powerScale !== 1.0) {
      enhanced = enhanced.replace(
        /S(\d+)/g,
        (match, power) => {
          const newPower = Math.round(parseInt(power) * powerScale);
          return `S${newPower}`;
        }
      );
    }

    // Add focus height
    if (focusHeight !== 0) {
      enhanced = enhanced.replace(
        /G0 Z/,
        `G0 Z${focusHeight.toFixed(3)} ; focus height\nG0 Z`
      );
    }

    return enhanced;
  }

  // ===== Calculate Time Estimate =====
  function calculateStats(toolpath) {
    let totalDistance = 0;
    let rapidDistance = 0;
    let cuttingDistance = 0;
    let totalTime = 0; // in seconds

    for (let i = 1; i < toolpath.length; i++) {
      const p1 = toolpath[i - 1];
      const p2 = toolpath[i];

      const dx = p2.x - p1.x;
      const dy = p2.y - p1.y;
      const dz = p2.z - p1.z;
      const distance = Math.sqrt(dx * dx + dy * dy + dz * dz);

      totalDistance += distance;

      if (p2.type === 'G0') {
        rapidDistance += distance;
        // Assume 5000 mm/min rapid
        totalTime += (distance / 5000) * 60;
      } else {
        cuttingDistance += distance;
        if (p2.f && p2.f > 0) {
          totalTime += (distance / p2.f) * 60;
        }
      }
    }

    return {
      totalDistance: Math.round(totalDistance * 100) / 100,
      rapidDistance: Math.round(rapidDistance * 100) / 100,
      cuttingDistance: Math.round(cuttingDistance * 100) / 100,
      estimatedTime: Math.round(totalTime),
      estimatedTimeFormatted: formatTime(totalTime)
    };
  }

  // ===== Format Time as HH:MM:SS =====
  function formatTime(seconds) {
    const hours = Math.floor(seconds / 3600);
    const minutes = Math.floor((seconds % 3600) / 60);
    const secs = Math.round(seconds % 60);

    return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(secs).padStart(2, '0')}`;
  }

  // ===== Export G-code to File =====
  function exportGCode(gcode, filename = 'program') {
    const extension = gcode.extension || '.gcode';
    const fullFilename = filename.endsWith(extension) ? filename : filename + extension;

    const blob = new Blob([gcode.gcode], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = fullFilename;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);

    return { success: true, filename: fullFilename };
  }

  // ===== Macro Substitution Engine =====
  function applyMacro(template, variables = {}) {
    let result = template;

    for (let [key, value] of Object.entries(variables)) {
      const regex = new RegExp(`\\$${key}`, 'g');
      result = result.replace(regex, value);
    }

    return result;
  }

  // ===== Validate G-code =====
  function validateGCode(gcode) {
    const errors = [];
    const warnings = [];
    const lines = gcode.split('\n');

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i].trim();

      // Check for unmatched parentheses
      if ((line.match(/\(/g) || []).length !== (line.match(/\)/g) || []).length) {
        errors.push(`Line ${i + 1}: Unmatched parentheses in comment`);
      }

      // Check for rapid moves without Z clearance
      if (line.includes('G0') && !line.includes('Z')) {
        warnings.push(`Line ${i + 1}: G0 rapid move without explicit Z coordinate`);
      }

      // Check for duplicate M codes
      const mCodes = line.match(/M\d+/g);
      if (mCodes && new Set(mCodes).size !== mCodes.length) {
        warnings.push(`Line ${i + 1}: Duplicate M codes detected`);
      }
    }

    return { errors, warnings, isValid: errors.length === 0 };
  }

  // ===== Generate Tool Change Sequence =====
  function generateToolChangeSequence(tools = []) {
    const sequence = [];

    for (let i = 0; i < tools.length; i++) {
      const tool = tools[i];
      sequence.push({
        toolNum: tool.num,
        name: tool.name,
        changeCmd: `M6 T${tool.num}`,
        speedCmd: `M3 S${tool.rpm || 8000}`
      });
    }

    return sequence;
  }

  // ===== Public API =====
  window.cycleCAD = window.cycleCAD || {};
  window.cycleCAD.gcode = {
    // Core conversion
    toolpathToGCode: toolpathToGCode,
    formatLine: formatLine,
    exportGCode: exportGCode,

    // Mode-specific enhancements
    addFDMFeatures: addFDMFeatures,
    addCNCFeatures: addCNCFeatures,
    addLaserFeatures: addLaserFeatures,

    // Utilities
    calculateStats: calculateStats,
    formatTime: formatTime,
    applyMacro: applyMacro,
    validateGCode: validateGCode,
    generateToolChangeSequence: generateToolChangeSequence,

    // Presets and templates
    getPresets: () => PRESETS,
    getMacroTemplates: () => MACRO_TEMPLATES,

    // Version
    version: '1.0'
  };

  console.log('[G-code Generator] Engine loaded. Supported modes: cnc, fdm, laser');

})();
